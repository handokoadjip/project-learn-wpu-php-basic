	<h1>Composer</h1>
	<p>Composer adalah dependency managaer yang ada di PHP. Dependency/ kertegantungan yatu script php kita bergantung kepada orang lain, lebih dari satu library, plugin, atau framework orang lain dan script yang kita gunakan punya orang lain kemungkinan bergantung juga pada script yang lain jadi script nya saling bergantung</p>
	<p>kalau seperti itu cenderung akan sulit untuk mengelola nya apalagi ketergantungannya semakin besar. apalahgi ketika kita  bekerja dengan team. maka dari itu kita perlu dependency manager yaitu composer sehingga ketika ada update dari script orang lain kita akan mudah memanipulasi scriptnya</p>
	<p>Macam macam dependency amanager ada banyak untuk bahasa pemrograman yang lain</p>
	<ul>
		<li>Bower</li>
		<li>NPM(node package manager) - Node</li>
		<li>Yarn</li>
		<li>Bundler - ruby</li>
	</ul>
	<p>karena dependency manager hanya mengelola, maka di dalam dependency manager ini sudah terdapat tempat repositori untuk package/ source yang bisa digunakan di tempat yang bernama packagist</p>
